    /**
     * 生成绑定类名
     * "sqlite3" -> "Sqlite3FFI"
     */
    private String getBindingClassName() {
        List<LinkDirective> links = bindingTable.getAllLinks();
        if (links.isEmpty()) {
            return "ExternFFI";
        }
        String libName = links.get(0).libraryName;
        return toPascalCase(libName) + "FFI";
    }

    /**
     * 检查是否是 extern 定义的自定义类型
     */
    private boolean isExternType(String typeName) {
        return bindingTable.findType(typeName) != null;
    }

    private static String toPascalCase(String name) {
        if (name == null || name.isEmpty()) return name;
        StringBuilder sb = new StringBuilder();
        boolean capitalizeNext = true;
        for (char c : name.toCharArray()) {
            if (c == '_' || c == '-' || c == '.') {
                capitalizeNext = true;
            } else if (capitalizeNext) {
                sb.append(Character.toUpperCase(c));
                capitalizeNext = false;
            } else {
                sb.append(c);
            }
        }
        return sb.toString();
    }

    private static String toConstantName(String name) {
        return name.toUpperCase().replaceAll("[^A-Z0-9]", "_");
    }
