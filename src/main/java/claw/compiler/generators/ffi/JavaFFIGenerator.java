    /**
     * Claw FFI 类型 → Java 类型（Panama 策略）
     */
    public static String mapClawTypeToJavaType(String clawType) {
        if (clawType == null) return "void";

        switch (clawType) {
            case "Void":    return "void";
            case "Int":     return "int";
            case "Float":   return "double";
            case "String":  return "String";
            case "CString": return "String";
            case "Bool":    return "boolean";
            case "Any":     return "MemorySegment";

            case "Pointer":       return "MemorySegment";
            case "OpaquePointer": return "MemorySegment";
            case "FuncPointer":   return "MemorySegment";
            case "SizeT":         return "long";

            case "Int8":   return "byte";
            case "Int16":  return "short";
            case "Int32":  return "int";
            case "Int64":  return "long";
            case "UInt8":  return "byte";
            case "UInt16": return "short";
            case "UInt32": return "int";
            case "UInt64": return "long";

            default:
                if (clawType.startsWith("Ref<") || clawType.startsWith("CArray<")) {
                    return "MemorySegment";
                }
                return "MemorySegment";
        }
    }

    /**
     * Claw FFI 类型 → Java 方法参数类型（Panama 策略）
     * String 参数保持为 String（在方法内部转换）
     */
    private String mapClawTypeToJavaParamType(String clawType) {
        if ("String".equals(clawType) || "CString".equals(clawType)) {
            return "String";
        }
        return mapClawTypeToJavaType(clawType);
    }

    /**
     * Claw FFI 类型 → MemoryLayout（Panama FunctionDescriptor 用）
     */
    public static String mapClawTypeToMemoryLayout(String clawType) {
        if (clawType == null) return "ValueLayout.ADDRESS";

        switch (clawType) {
            case "Int":     return "ValueLayout.JAVA_INT";
            case "Float":   return "ValueLayout.JAVA_DOUBLE";
            case "Bool":    return "ValueLayout.JAVA_BOOLEAN";
            case "String":  return "ValueLayout.ADDRESS";
            case "CString": return "ValueLayout.ADDRESS";
            case "Any":     return "ValueLayout.ADDRESS";

            case "Pointer":       return "ValueLayout.ADDRESS";
            case "OpaquePointer": return "ValueLayout.ADDRESS";
            case "FuncPointer":   return "ValueLayout.ADDRESS";
            case "SizeT":         return "ValueLayout.JAVA_LONG";

            case "Int8":   return "ValueLayout.JAVA_BYTE";
            case "Int16":  return "ValueLayout.JAVA_SHORT";
            case "Int32":  return "ValueLayout.JAVA_INT";
            case "Int64":  return "ValueLayout.JAVA_LONG";
            case "UInt8":  return "ValueLayout.JAVA_BYTE";
            case "UInt16": return "ValueLayout.JAVA_SHORT";
            case "UInt32": return "ValueLayout.JAVA_INT";
            case "UInt64": return "ValueLayout.JAVA_LONG";

            default:
                if (clawType.startsWith("Ref<") || clawType.startsWith("CArray<")) {
                    return "ValueLayout.ADDRESS";
                }
                return "ValueLayout.ADDRESS";
        }
    }

    /**
     * Claw FFI 类型 → JNI 类型（JNI 降级策略）
     */
    private String mapClawTypeToJNIType(String clawType) {
        if (clawType == null) return "void";

        switch (clawType) {
            case "Void":    return "void";
            case "Int":     return "int";
            case "Float":   return "double";
            case "String":  return "String";
            case "CString": return "String";
            case "Bool":    return "boolean";
            case "Any":     return "long";

            case "Pointer":       return "long";
            case "OpaquePointer": return "long";
            case "SizeT":         return "long";

            case "Int8":   return "byte";
            case "Int16":  return "short";
            case "Int32":  return "int";
            case "Int64":  return "long";

            default:
                if (clawType.startsWith("Ref<") || clawType.startsWith("CArray<")) {
                    return "long";  // 指针作为 long 传递
                }
                return "long";
        }
    }

    /**
     * 生成 Java 运行时平台检测
     */
    public String generatePlatformDetection() {
        StringBuilder sb = new StringBuilder();

        sb.append("/**\n");    sb.append(" * Claw FFI 平台检测工具\n");
        sb.append(" */\n");
        sb.append("final class ClawPlatform {\n");
    
        sb.append("    enum OS { WINDOWS, LINUX, MACOS, FREEBSD, ANDROID, UNKNOWN }\n");
        sb.append("    enum Arch { X86_64, ARM64, X86, ARM, UNKNOWN }\n");
    
        sb.append("    static final OS CURRENT_OS;\n");
        sb.append("    static final Arch CURRENT_ARCH;\n");
    
        sb.append("    static {\\n");
        sb.append("        String osName = System.getProperty(\"os.name\", \"\").toLowerCase();\n");
        sb.append("        if (osName.contains(\"win\"))        CURRENT_OS = OS.WINDOWS;\n");
        sb.append("        else if (osName.contains(\"mac\"))   CURRENT_OS = OS.MACOS;\n");
        sb.append("        else if (osName.contains(\"linux\")) CURRENT_OS = OS.LINUX;\n");
        sb.append("        else if (osName.contains(\"freebsd\")) CURRENT_OS = OS.FREEBSD;\n");
        sb.append("        else                                CURRENT_OS = OS.UNKNOWN;\n");
    
        sb.append("        String arch = System.getProperty(\"os.arch\", \"\").toLowerCase();\n");
        sb.append("        if (arch.contains(\"amd64\") || arch.contains(\"x86_64\")) CURRENT_ARCH = Arch.X86_64;\n");
        sb.append("        else if (arch.contains(\"aarch64\") || arch.contains(\"arm64\")) CURRENT_ARCH = Arch.ARM64;\n");
        sb.append("        else if (arch.contains(\"x86\") || arch.contains(\"i386\")) CURRENT_ARCH = Arch.X86;\n");
        sb.append("        else if (arch.contains(\"arm\"))     CURRENT_ARCH = Arch.ARM;\n");
        sb.append("        else                                CURRENT_ARCH = Arch.UNKNOWN;");
        sb.append("    }\n");
    
        sb.append("    static boolean isPlatform(OS... targets) {\n");
        sb.append("        for (OS t : targets) if (t == CURRENT_OS) return true;\n");
        sb.append("        return false;\n");
        sb.append("    }\n");
    
        sb.append("    static boolean isArch(Arch... targets) {\n");
        sb.append("        for (Arch t : targets) if (t == CURRENT_ARCH) return true;\n");
        sb.append("        return false;\n");
        sb.append("    }\n");
    
        // 库文件名生成
        sb.append("    static String libraryFileName(String name) {\n");
        sb.append("        return switch (CURRENT_OS) {");
        sb.append("            case WINDOWS -> name + \".dll\";\n");
        sb.append("            case MACOS   -> \"lib\" + name + \".dylib\";\n");
        sb.append("            default      -> \"lib\" + name + \".so\";\n");
        sb.append("        };\n");
        sb.append("    }\n");
    
        sb.append("}new\n");
    
        return sb.toString();
    }
    
    /**
     * 生成平台条件绑定（Java Panama）
     *
     * Java 无法用预处理器，使用运行时判断 + 策略模式
     */
    public String generatePlatformConditionalBindings(FFIBindingTable fullTable) {
        StringBuilder sb = new StringBuilder();
    
        sb.append("/**\n");
        sb.append(" * 平台自适应的 FFI 绑定加载\n");
        sb.append(" */\n");
        sb.append("static void loadPlatformBindings() {\n");
    
        boolean first = true;
        for (FFIBindingTable.ExternBlock block : fullTable.getExternBlocks()) {
            if (block.platform != null && !block.platform.isUniversal()) {
                if (!first) sb.append("    else ");
                else sb.append("    ");
                first = false;
    
                sb.append(generateJavaPlatformCondition(block.platform)).append(" {\\n");
    
                // 加载该平台的库和函数
                for (FFIBindingTable.LinkDirective link : block.links) {
                    sb.append("        loadLibrary(\"").append(link.libraryName).append("\");\n");
                }
                for (FFIBindingTable.ExternFunction func : block.functions) {
                    sb.append("        bindFunction(\"").append(func.name).append("\");\n");
                }
    
                sb.append("    }\n");
            }
        }
    
        sb.append("}\n");
        return sb.toString();
    }
    
    private String generateJavaPlatformCondition(FFIBindingTable.PlatformConstraint constraint) {
        List<String> conditions = new ArrayList<>();
    
        for (String p : constraint.getPlatforms()) {
            conditions.add("ClawPlatform.isPlatform(ClawPlatform.OS."
                + p.toUpperCase() + ")");
        }
        for (String a : constraint.getArchitectures()) {
            conditions.add("ClawPlatform.isArch(ClawPlatform.Arch."
                + a.toUpperCase() + ")");
        }
    
        return "if (" + String.join(" && ", conditions) + ")";
    }
    

